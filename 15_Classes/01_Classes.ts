
/*


TypeScript is object oriented JavaScript.
TypeScript supports object-oriented programming features like classes,
interfaces, etc. A class in terms of OOP is a blueprint for creating objects.
A class encapsulates data for the object. 
Typescript gives built in support for this concept called class. 
JavaScript ES5 or earlier didn’t support classes. 
Typescript gets this feature from ES6.


Syntax
class class_name { 
   //class scope 
}


A class definition can include the following −

Fields − A field is any variable declared in a class. 
Fields represent data pertaining to objects

Constructors − Responsible for allocating memory for the objects of the class

Functions − Functions represent actions an object can take. 
They are also at times referred to as methods


*/